@router.get("/samples")
def list_samples():
    """Returns a list of available sample datasets."""
    return {
        "samples": [
            {"id": k, "name": v["name"], "target": v["target"]}
            for k, v in DATASET_REGISTRY.items()
        ]
    }

@router.post("/sample/{dataset_id}")
async def analyze_sample_dataset(
    dataset_id: str,
    target_column: str = Query(default=None),
    user: Optional[dict] = Depends(get_optional_user)
):
    """Analyzes a sample dataset and saves the result to the database."""
    try:
        df, dataset_name, default_target = get_sample_df(dataset_id)

        target_override = target_column if target_column else default_target
        filename = f"{dataset_id}_sample.csv"

        # Run the core analysis
        result, target = _run_full_analysis(df, target_override, filename)

        if isinstance(result, dict) and "error" in result:
            return JSONResponse(result, status_code=400)

        # Prepare database document
        doc = {
            "filename": filename,
            "dataset_name": dataset_name,
            "result": clean_for_json(result),
            "created_at": datetime.utcnow(),
            "is_sample": True
        }

        if user:
            doc["user_id"] = user["_id"]

        # Save to DB
        try:
            reports_collection.insert_one(doc)
        except Exception as e:
            logger.error(f"DB save error: {e}")

        return JSONResponse(content=clean_for_json(result))

    except Exception as e:
        logger.error(f"Sample analysis error: {traceback.format_exc()}")
        return JSONResponse({
            "error": str(e),
            "traceback": traceback.format_exc()
        }, status_code=500)
