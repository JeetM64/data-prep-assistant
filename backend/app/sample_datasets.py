def get_sample_df(dataset_id: str):
    """Returns (df, name, default_target) or raises ValueError."""
    entry = DATASET_REGISTRY.get(dataset_id)
    if not entry:
        raise ValueError(f"Unknown sample dataset: '{dataset_id}'. Available: {list(DATASET_REGISTRY.keys())}")
    df = pd.read_csv(io.StringIO(entry["csv"]))
    return df, entry["name"], entry["target"]

